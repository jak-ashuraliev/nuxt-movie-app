      <p class="lead">{{ subtitle }}</p>
      <b-alert
        :show="dismissCountDown"
        dismissible
        variant="danger"
        @dismissed="dismissCountDown = 0"
        @dismiss-count-down="countDownChanged"
        class="mb-3"
      >
        <b-container class="justify-content-md-center">
          <!-- <b-col md="8"> -->
          {{ message }}
          <b-progress
            variant="danger"
            :max="dismissSecs"
            :value="dismissCountDown"
          ></b-progress>
          <!-- </b-col> -->
        </b-container>
      </b-alert>
      <!-- v-if show danger button  -->
      <b-button
        v-if="showBtn"
        @click="showAlert"
        variant="danger"
        size="lg"
        class="m-1"
